public class VariablesAndConstants {

    public static void main(String[] args) {

        // Variables

        String name = "Jonathan";
        System.out.println(name);

        name = "Israel";
        System.out.println(name);

        //name = 37; Error (no podemos cambiar un tipo de dato)

        int age = 21;
        System.out.println(age);

        var email = "israelruales7@gmail.com";
        System.out.println(email);

        var year = 2025;
        System.out.println(year);

        //Constantes

        final String EMAIL = "israelruales7@gmail.com";
        // EMAIL = "jdevlogic@gmail.com"; Es una constante.
        System.out.println(EMAIL);
    }
}
