# Creamos unas funciones para el modulo
# Son funciones que se pueden usar en otros modulos


def saludar (nombre):                   # Creamos la funcion saludar entre parentesis el "parametro"
    print(f"Hola, {nombre}")            # El mensaje para saludar en este caso

def sumar (num1, num2):                 # Creamos un funcion para que dos numero se sumen
    return num1 + num2                  # Hacemos que los dos numeros se sumen y guardamos su valor con "return"

def restar (num1 , num2):               # Creamos un funcion para que dos numero se resten
    return num1 - num2                  # Y que el resultado se guarde con "return"
   
