# Para crear set se usan {}

lenguajes = {"Python" , "Java" , "C++" , "Python"}          # Creamos un set con {} 

print(lenguajes)                    # Al imprimir la curiosidad que tienen los set es que no muestra duplicados

lenguajes.add("JavaScript")         # Para añadir al set seria con .add(elemento añadir)

lenguajes.remove("C++")             # Para borrar un elemento con .remove(elemento)

print(lenguajes)                    # Imprimimos para asegurarnos que de que todo funciono bien




