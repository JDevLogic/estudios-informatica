# int ()                    # Convierte un número a entero
# float ()                  # Convierte un número a decimal 
# str ()                    # Convierte un número a cadena
# bool ()                   # Convierte un número a booleano

print (bool(""))                  # Devuelve False porque la cadena está vacía
print (bool("0"))                 # Devuelve True porque la cadena no está vacía
print (bool(None))                # Devuelve False porque None es un valor nulo
print (bool(" "))                 # Devuelve True porque la cadena no está vacía (tiene un espacio)
print (bool(0))                   # Devuelve False porque el numero 0 es considerado como False