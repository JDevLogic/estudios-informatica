def suma (a , b):
    resultado = a + b
    return resultado                        # La función suma recibe dos argumentos y devuelve la suma de ambos y return guarda el resultado en la variable resultado

c = suma(1,2)                               # Llamamos a la función suma con los argumentos 1 y 2 y guardamos el resultado en la variable c

d = suma (c,2)                              # Llamamos a la función suma con los argumentos c y 2 y guardamos el resultado en la variable d

print(d)                                    # Imprimimos el resultado de la suma    
